<template>
  <div class="">
    <h1>{{id? '编辑用户':'新增用户'}}</h1>
    <el-form lable-width="120px" @submit.native.prevent="save">
      <el-form-item label="名称">
        <el-input v-model="model.name"></el-input>
      </el-form-item>

      <el-form-item label="头像">
        <el-upload
            class="avatar-uploader"
            :action="$http.defaults.baseURL +'/upload'"
            :headers="getAuthHeaders()"
            :show-file-list="false"
            :on-success="afterUpload"
        >
          <img v-if="model.avatar" :src="model.avatar" class="avatar ell">
          <i v-else class="ell el-icon-plus avatar-uploader-icon" ></i>
        </el-upload>
      </el-form-item>

      <el-form-item label="用户名">
        <el-input v-model="model.username"></el-input>
      </el-form-item>
      <el-form-item label="密码">
        <el-input v-model="model.password"></el-input>
      </el-form-item>
      <el-form-item label="类型">
        <el-input v-model="model.type"></el-input>
      </el-form-item>
      <el-form-item>
        <el-button type="primary" native-type="submit">保存</el-button>
      </el-form-item>
    </el-form>
  </div>
</template>

<script>
  export default {
    props:{
      id:{}
    },
    data() {
      return{
        model:{}
      }
    },
    methods:{
      afterUpload(res){
        this.$set(this.model,'avatar',res.url)

      },
      async save(){
        let res
        if(this.id){
          res = await this.$http.put(`rest/user/${this.id}`,this.model)
        }else{
          res = await this.$http.post('rest/user',this.model)
        }

        this.$router.push('/user/list')
        this.$message({
          type:'success',
          message:'保存成功'
        })
      },

      async fetch(){
        const res = await this.$http.get(`rest/user/${this.id}`)
        this.model = res.data
      }
    },

    created() {
      this.id && this.fetch()
    }
  };
</script>

